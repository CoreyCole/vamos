package generate

import (
	"bytes"
	"fmt"
	"go/format"
	"path/filepath"
	"sort"
	"strings"

	"github.com/CoreyCole/vamos/pkg/e2e/steps"
	"github.com/CoreyCole/vamos/pkg/e2e/story"
)

type Options struct {
	StoryDir     string
	OutputDir    string
	PackageName  string
	StepCatalog  steps.Catalog
	FixtureNames []string
}

type Result struct {
	Files   []GeneratedFile
	Changed bool
}

type GeneratedFile struct {
	Path    string
	Content []byte
}

func Generate(features []story.Feature, opts Options) (Result, error) {
	if opts.PackageName == "" {
		opts.PackageName = "generated"
	}
	if opts.StepCatalog == nil {
		opts.StepCatalog = steps.DefaultCatalog()
	}
	sort.Slice(
		features,
		func(i, j int) bool { return features[i].Slug < features[j].Slug },
	)

	result := Result{}
	for _, feature := range features {
		content, err := renderFeature(feature, opts)
		if err != nil {
			return Result{}, err
		}
		filename := strings.ReplaceAll(feature.Slug, "-", "_") + "_e2e_test.go"
		result.Files = append(result.Files, GeneratedFile{
			Path:    filepath.Join(opts.OutputDir, filename),
			Content: content,
		})
	}
	return result, nil
}

func renderFeature(feature story.Feature, opts Options) ([]byte, error) {
	var b bytes.Buffer
	fmt.Fprintln(&b, "// Code generated by vamos e2e generate; DO NOT EDIT.")
	fmt.Fprintf(&b, "package %s\n\n", opts.PackageName)
	fmt.Fprintln(&b, "import (")
	fmt.Fprintln(&b, "\t\"testing\"")
	fmt.Fprintln(&b)
	fmt.Fprintln(&b, "\te2e \"github.com/CoreyCole/vamos/pkg/e2e/runtime\"")
	fmt.Fprintln(&b, "\t\"github.com/CoreyCole/vamos/pkg/e2e/steps\"")
	fmt.Fprintln(&b, ")")
	fmt.Fprintln(&b)

	for _, scenario := range feature.Scenarios {
		if err := renderScenario(&b, feature, scenario, opts); err != nil {
			return nil, err
		}
	}
	properties, err := story.ExpandProperties(feature)
	if err != nil {
		return nil, err
	}
	for _, scenario := range properties {
		if err := renderScenario(&b, feature, scenario, opts); err != nil {
			return nil, err
		}
	}
	out, err := format.Source(b.Bytes())
	if err != nil {
		return nil, fmt.Errorf(
			"format generated %s: %w\n%s",
			feature.Slug,
			err,
			b.String(),
		)
	}
	return out, nil
}

func renderScenario(
	b *bytes.Buffer,
	feature story.Feature,
	scenario story.Scenario,
	opts Options,
) error {
	fmt.Fprintf(
		b,
		"func Test%s_%s(t *testing.T) {\n",
		exported(feature.Slug),
		exported(scenario.Slug),
	)
	if scenario.Viewport != "" {
		fmt.Fprintf(
			b,
			"\te2e.RunScenarioWithViewport(t, %q, %q, e2e.%s, func(t testing.TB, ctx *e2e.Context) {\n",
			feature.Slug,
			scenario.Slug,
			viewportConst(scenario.Viewport),
		)
	} else {
		fmt.Fprintf(
			b,
			"\te2e.RunScenario(t, %q, %q, func(t testing.TB, ctx *e2e.Context) {\n",
			feature.Slug,
			scenario.Slug,
		)
	}
	allSteps := append(
		append(append([]story.Step{}, scenario.Given...), scenario.When...),
		scenario.Then...)
	for _, step := range allSteps {
		def, _, err := opts.StepCatalog.Resolve(step)
		if err != nil {
			return err
		}
		call, err := def.Compile(step)
		if err != nil {
			return err
		}
		fmt.Fprintf(b, "\t\tsteps.%s(t, ctx", call.Function)
		for _, arg := range call.Args {
			fmt.Fprintf(b, ", %s", arg)
		}
		fmt.Fprintln(b, ")")
	}
	fmt.Fprintln(b, "\t})")
	fmt.Fprintln(b, "}")
	fmt.Fprintln(b)
	return nil
}

func viewportConst(viewport string) string {
	switch viewport {
	case "mobile":
		return "ViewportMobile"
	case "desktop-half":
		return "ViewportDesktopHalf"
	case "desktop-full":
		return "ViewportDesktopFull"
	default:
		return "ViewportDesktopFull"
	}
}

func exported(slug string) string {
	parts := strings.Split(slug, "-")
	for i, p := range parts {
		if p != "" {
			parts[i] = strings.ToUpper(p[:1]) + p[1:]
		}
	}
	return strings.Join(parts, "")
}
